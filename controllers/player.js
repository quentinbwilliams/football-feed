class Player {
  constructor(
    name,
    age,
    nationality,
    photo,
    teamName,
    teamID,
    number,
    position
  ) {
    this.name = name;
    this.age = age;
    this.nationality = nationality;
    this.photo = photo;
    this.teamName = teamName;
    this.teamID = teamID;
    this.number = number;
    this.position = position;
  }
}

module.exports = Player;
